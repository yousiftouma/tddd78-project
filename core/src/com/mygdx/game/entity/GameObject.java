package com.mygdx.game.entity;

/**
 * Enum representing each object in the gameworld
 */
public enum GameObject {
    /**
     * Walls, floors, ceilings, paltforms
     */
    WALL,
    /**
     * Enemy
     */
    ENEMY,
    /**
     * Player
     */
    PLAYER,
    /**
     * Small notmoving coin
     */
    SMALL_STATIC_COIN,
    /**
     * Small moving coin
     */
    SMALL_MOVING_COIN


}
